package review0824;

public class Review_For {

    public static void main(String[] args) {

        //if-else문
        int num1 = 10;
        if (num1 == 10) {
            System.out.println("num은 10입니다."); // R: num이 10입니다.
        } else {
            System.out.println("num은 10이 아닙니다."); // R: 결과 없음.
        }
        //for문

        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " "); // R: 1 2 3 ... 8 9 10 
        }

    }
}
